<template>
  <div class="hello">
    <el-date-picker
      v-model="value1"
      type="date"
      placeholder="开始时间"
      clearable
      @change="handle"
    >
    </el-date-picker>
    <el-date-picker
      v-model="value2"
      type="date"
      placeholder="结束时间"
      @change="handle"
    >
    </el-date-picker>
  </div>
</template>

<script>
export default {
  data() {
    return {
      pickerOptions: {
        disabledDate(time) {
          return time.getTime() > Date.now()
        },
        shortcuts: [
          {
            text: '今天',
            onClick(picker) {
              picker.$emit('pick', new Date())
            }
          },
          {
            text: '昨天',
            onClick(picker) {
              const date = new Date()
              date.setTime(date.getTime() - 3600 * 1000 * 24)
              picker.$emit('pick', date)
            }
          },
          {
            text: '一周前',
            onClick(picker) {
              const date = new Date()
              date.setTime(date.getTime() - 3600 * 1000 * 24 * 7)
              picker.$emit('pick', date)
            }
          }
        ]
      },
      value1: '',
      value2: ''
    }
  },
  methods: {
    handle() {
      console.log(this.value1)
    }
  }
}
</script>

